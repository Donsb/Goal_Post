import UIKit

extension UIButton {
    
    /* Set Selected Colour Function. */
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.5603182912, blue: 0, alpha: 1)
    } // END Set Selected Colour.
    
    
    /* Set Deselected Colour Function. */
    func setDesectlectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6980392157, green: 0.8666666667, blue: 0.6862745098, alpha: 1)
    } // END Set Deselected Colour.
    
} // END Extension.


